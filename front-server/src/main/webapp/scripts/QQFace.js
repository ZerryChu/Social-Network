		function replace_em(str) {
			str = str.replace(/\</g, '<；');
			str = str.replace(/\>/g, '>；');
			str = str.replace(/\n/g, '<；br/>；');
			str = str.replace(/\[em_([0-9]*)\]/g,
					'<img src="face/$1.gif" border="0" />');
			return str;
		}
